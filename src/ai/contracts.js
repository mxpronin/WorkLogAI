export function buildPrompt(kind, editablePrompt, payload) {
  const roleAwarePrompt = withRolePrefix(editablePrompt || DEFAULT_PROMPTS[kind]);
  return `${roleAwarePrompt}\n\n${contracts[kind]}\n\nНеизменяемые данные приложения:\n${JSON.stringify(payload)}`;
}

export function withRolePrefix(prompt) {
  const text = String(prompt).trim();
  return text.startsWith(ROLE_PREFIX) || /ведущ(?:ий|его)\s+программист(?:а)?\s+1с/i.test(text) || /ведущий\s+аналитик/i.test(text) ? text : `${ROLE_PREFIX}\n\n${text}`;
}

function parseJson(text) {
  const cleaned = String(text ?? '').trim().replace(/^```(?:json)?\s*/i, '').replace(/\s*```$/, '');
  try { return JSON.parse(cleaned); } catch { throw new Error('ИИ вернул ответ не в формате JSON. Попробуйте ещё раз или измените шаблон.'); }
}

function asWarnings(value) {
  return Array.isArray(value) ? value.filter((item) => typeof item === 'string').map((item) => item.trim()).filter(Boolean).slice(0, 10) : [];
}

export function parseAiResult(kind, text) {
  const result = parseJson(text);
  if (result?.schemaVersion !== PROMPT_VERSION) throw new Error('ИИ вернул неподдерживаемую версию ответа.');
  if (kind === 'entry' || kind === 'task') {
    if (typeof result.spprDescription !== 'string' || !result.spprDescription.trim()) throw new Error('В ответе ИИ нет описания СППР.');
    return { spprDescription: result.spprDescription.trim(), warnings: asWarnings(result.warnings) };
  }
  if (kind === 'audio') {
    if (typeof result.transcript !== 'string' || !result.transcript.trim() || typeof result.spprDescription !== 'string' || !result.spprDescription.trim()) throw new Error('В ответе ИИ нет расшифровки или описания СППР.');
    return { transcript: result.transcript.trim(), spprDescription: result.spprDescription.trim(), warnings: asWarnings(result.warnings) };
  }
  if (!Array.isArray(result.entries) || !Array.isArray(result.allocations)) throw new Error('В ответе ИИ нет записей или распределения.');
  return {
    entries: result.entries.filter((item) => typeof item?.entryId === 'string' && typeof item?.spprDescription === 'string').map((item) => ({ entryId: item.entryId, spprDescription: item.spprDescription.trim() })).filter((item) => item.spprDescription),
    allocations: result.allocations.filter((item) => typeof item?.taskId === 'string' && Number.isInteger(item?.minutes) && item.minutes >= 0).map((item) => ({ taskId: item.taskId, minutes: item.minutes, reason: typeof item.reason === 'string' ? item.reason.trim() : '' })),
    warnings: asWarnings(result.warnings),
  };
}
